const questionEl = document.getElementById('question');
const answerEls = document.querySelectorAll('.answer');
const quiz = document.getElementById('quiz');                   //Pobieranie Danych z HTML
const a_ = document.getElementById('a_t');
const b_= document.getElementById('b_t');
const c_ = document.getElementById('c_t');
const d_ = document.getElementById('d_t');
const submit=document.getElementById('submit');

let nickname = sessionStorage.getItem('nick');

let current = 0;
let score = 0;                           //Zmienne do wyniku i do liczenia pytan
load();

function load(){
     quizData.sort(()=> Math.random() - 0.5) ;              //Losowanie pytan
     des();  
    const currentQuizData=quizData[current];                
    questionEl.innerText=currentQuizData.question;          //Wrzucanie pytan i odpowiedzi w odpowiednie miejsce w HTML
    a_.innerText = currentQuizData.a;
    b_.innerText = currentQuizData.b;
    c_.innerText = currentQuizData.c;
    d_.innerText = currentQuizData.d;
   
     
}

function clear(){
    sessionStorage.clear();                     //Zerowanie sessionStorage
}

function selected(){
     let answer = undefined;

     answerEls.forEach((answerEl) =>{                      // Sprawdzanie czy wybrano odp.     
          if(answerEl.checked){
            answer = answerEl.id;
          }
     }); 

     return answer;
}

function des(){
    answerEls.forEach((answerEl) =>{                        //Odznaczanie odpowiedzi przy nastepnym pytaniu
      answerEl.checked=false;
   }); 
}

submit.addEventListener('click', () => {
    const answer = selected();
         
    if(answer){
        if(answer === quizData[current].correct){                   //sprawdzanie poprawnosci odpowiedzi
            score++;
        }
        current++; 
        count();      
        if(current===10){ 
            document.getElementById('count').style.display='none';                               //Wylaczanie quizu po 10 pytaniach
            document.getElementById('question').style.display='none';
            quiz.innerHTML=`<h2> ${nickname} twoj wynik to: ${score}/10  \n
            <button class="reset" onclick="reset()">Resetuj</button>`
        }
        else if(current < quizData.length){
            load();
            }
        } 
});

function count(){
    document.getElementById('count').innerHTML=current+"/10"; 
}

function reset(){
    location.reload();                      //Restart
   
}
